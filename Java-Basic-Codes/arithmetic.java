public class arithmetic{
  public static void main(String[]args){
    int x = 12;
    int y = 25;
    int sum = x+y;
    int diff = y-x;
    int prd = x*y;
    int q = y/x;
    int r = y%x;
    System.out.println("Sum of " + x + " and " + y + " is " + sum);
    System.out.println("Difference between " + x + " and " + y + " is " + diff);
    System.out.println("Product of " + x + " and " + y + " is " + prd);
    System.out.println("Quotient when " + y + " is divided by " + x + " is " + q);
    System.out.println("Remainder when " + y + " is divided by " + x + " is " + r);
  }
}
